package com.javabasictraining;

public class Comparison {

    public static void main(String[] args) {

        //  ==  equal to
        //  !=  not equal to
        //  >   greater than
        //  >=  greater than or equal to
        //  <   less than
        //  <=  less than or equal to

        int a = 3;
        int b = 5;

        if (a == b);
        System.out.println ("a == b");

        if (a != b);
        System.out.println ("a != b");

        if (a > b);
        System.out.println (a > b);

        if (a >= b);
        System.out.println(a >= b);

        if (a < b);
        System.out.println(a < b);

        if (a <= b);
        System.out.println(a <= b);

    }
}
