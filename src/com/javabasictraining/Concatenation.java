package com.javabasictraining;

public class Concatenation {

    public static void main(String[] args) {

        int a = 15;
        int b = 4;

        int C = a * b;

        System.out.println("This is a math problem sum of 4 & 15 = " + C);


        double d = 15;
        double e = 4;

        double f = d / e;

        System.out.println("This is a math problem sum of 4 & 15 = " + f);
    }
}
